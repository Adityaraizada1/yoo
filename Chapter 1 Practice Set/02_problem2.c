/* Finding the area of the circle from C lang*/

#include <stdio.h>

int main() {
    int r = 7; // radius of the circle
    int height = 10; // height of the cylinder 
    printf("The area of the circle with radius %d is %f", r, 3.14 * r * r); // area of the circle
    printf("\nThe area of the cylinder with radius %d and height %d is %f", r, height, 3.14 * r * r * height); // area of the cylinder
    return 0;
}
