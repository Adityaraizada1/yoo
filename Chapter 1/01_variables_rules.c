/*
Rules for Naming Variables in C Language:

1. Variable names can contain letters (a-z, A-Z), digits (0-9), and underscores (_).
2. The first character must be a letter or underscore, not a digit.
3. No spaces or special characters (like @, #, $, etc.) are allowed.
4. C is case-sensitive: 'num' and 'Num' are different variables.
5. Reserved keywords (like int, float, return) cannot be used as variable names.

Example:

Valid variable names:
    int age;
    float salary_2024;
    char _grade;

Invalid variable names:
    int 2ndNumber;    // starts with a digit
    float total$;     // contains special character $
    char return;      // 'return' is a keyword
*/